// Faça cinco programas, um para cada operação aritmética básica.
// Seu programa deve ter duas constantes, a e b , definidas no começo com os valores que serão operados. Faça programas para:
//      Adição (a + b)
//      Subtração (a - b)
//      Multiplicação (a * b)
//      Divisão (a / b)
//      Módulo (a % b)

let a = 10;
let b = 2;

// Adição
resultado = a + b;
console.log('O resultado de ' + a + ' mais ' + b + ' é igual a: ' + resultado);

// Substração
resultado = a - b;
console.log('O resultado de ' + a + ' menos ' + b + ' é igual a: ' + resultado);

// Multiplicação
resultado = a * b;
console.log('O resultado de ' + a + ' vezes ' + b + ' é igual a: ' + resultado);

// Divisão
resultado = a / b;
console.log('O resultado de ' + a + ' dividido por ' + b + ' é igual a: ' + resultado);

// Módulo
resultado = a % b;
console.log('O resto da divisão de ' + a + ' dividido por ' + b + ' é igual a: ' + resultado);